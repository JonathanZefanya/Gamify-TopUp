<script>

    jQuery(document).on('click', '.copy-code', function () {
        var node = jQuery(this).siblings('.language-bash-inner').find('code')[0];

        var range = document.createRange();
        range.selectNode(node);

        window.getSelection().removeAllRanges();
        window.getSelection().addRange(range);

        document.execCommand('copy');

        window.getSelection().removeAllRanges();
        Notiflix.Notify.success("Copied Successfully");
    });

    document.addEventListener("DOMContentLoaded", function () {
        const observerOptions = {
            root: null,
            rootMargin: "0px",
            threshold: 0.5
        };

        const observer = new IntersectionObserver((entries) => {
            entries.forEach(entry => {
                const sectionId = entry.target.getAttribute("id");
                const link = document.querySelector(`a[href="#${sectionId}"]`);

                if (entry.isIntersecting) {
                    // Remove "highlight" from all links first
                    document.querySelectorAll(".highlight").forEach(el => el.classList.remove("highlight"));

                    // Add "highlight" to the current visible section's link
                    if (link) {
                        link.classList.add("highlight");
                    }
                }
            });
        }, observerOptions);

        // Observe each docs-section
        document.querySelectorAll(".docs-section").forEach(section => {
            observer.observe(section);
        });
    });

</script>
